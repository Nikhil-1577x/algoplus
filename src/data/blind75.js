export const getBlind75Progress = (solvedSlugs) => {
    const solved = BLIND_75.filter(problem =>
        solvedSlugs.includes(problem.slug)
    );

    return {
        total: BLIND_75.length,
        solved: solved.length,
        percentage: Math.round((solved.length / BLIND_75.length) * 100),
        remaining: BLIND_75.length - solved.length,
        solvedProblems: solved,
        remainingProblems: BLIND_75.filter(p => !solvedSlugs.includes(p.slug))
    };
};
